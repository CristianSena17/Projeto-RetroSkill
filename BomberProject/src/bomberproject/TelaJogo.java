            //System.out.println("Nome: "+BomberProject.getNome());
            
            
            
            
            sight='D';
            sight2='U';
            TP1.setIcon(new ImageIcon(getClass().getResource("tank/TANKRED/down 1.png")));
            TP2.setIcon(new ImageIcon(getClass().getResource("tank/TANKBLUE/up 1.png")));
            
