            vidaEscuro1.setSize(0, 32);
            BomberProject.explode2.vidap2=0;
            vidaEscuro2.setSize(0, 32);
            
        player1.setLocation(BomberProject.move.compx, BomberProject.move.compy);
        player2.setLocation(BomberProject.move2.compx, BomberProject.move2.compy);
