    public static void inserir(String Nome, int pontos) {
       // System.out.println("Valor final de pontos: "+ pontos);
       // System.out.println("nome final é: "+ Nome);
        
        
        boolean n=false;
        
        try {
            Connection conexao = Conexao.getConexao();
            Statement st = conexao.createStatement();
            
            String sql = "SELECT * FROM pontuacao where Usuario_Nome='"+Nome+"' and jogo_id=2";
            
            
            ResultSet rs = st.executeQuery(sql);
            
            n=rs.isBeforeFirst();
            
            if (!n){
                
                try {

                    //System.out.println("inseriu novo");
                    String sql2 = "INSERT INTO pontuacao VALUES ('"+ Nome +"',2,"+ pontos +")";

                    st.executeUpdate(sql2);

                }
                catch (Exception e) {
                    e.printStackTrace();
                }

            }else{
                try {

                    //System.out.println("Fez update");
                    String sql3 = "UPDATE pontuacao SET pontos = pontos + '"+ pontos +"' WHERE Usuario_Nome='"+Nome+"' and jogo_id=2";
                    
                    st.executeUpdate(sql3);

                }
                catch (Exception e) {
                    e.printStackTrace();
                }
            }   
            rs.close();
            st.close();
            Conexao.closeAll(conexao);
        }
        catch (Exception e) {
            e.printStackTrace();
            
        }
        

    }
    
